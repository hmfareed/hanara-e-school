// POST /api/staff/registration-code  (superadmin only)
const generateRegistrationCode = async (req, res, next) => {
  try {
    const RegistrationCode = require('../models/RegistrationCode');

    // Deactivate all previous active codes
    await RegistrationCode.updateMany({ isActive: true }, { $set: { isActive: false } });

    // Generate a random 6-digit numeric code
    const code = String(Math.floor(100000 + Math.random() * 900000));

    const newCode = await RegistrationCode.create({
      code,
      createdBy: req.user.id,
      isActive: true,
      isUsed: false,
    });

    logger.info(`Registration code generated by superadmin (${req.user.email}): ${code}`);

    res.status(201).json({
      success: true,
      message: 'New registration code generated successfully.',
      data: { code: newCode.code, createdAt: newCode.createdAt },
    });
  } catch (error) {
    next(error);
  }
};

// GET /api/staff/registration-code  (superadmin only)
