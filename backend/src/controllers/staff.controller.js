const Staff = require('../models/Staff');
const User = require('../models/User');
const bcrypt = require('bcryptjs');
const logger = require('../utils/logger');

// GET /api/staff
const getStaff = async (req, res, next) => {
  try {
    const { page = 1, limit = 20, role, status, search } = req.query;

    const filter = {};
    if (role) filter.role = role;
    if (status) filter.employmentStatus = status;
    if (search) {
      filter.$or = [
        { firstName: { $regex: search, $options: 'i' } },
        { lastName: { $regex: search, $options: 'i' } },
        { email: { $regex: search, $options: 'i' } },
      ];
    }

    const skip = (parseInt(page) - 1) * parseInt(limit);
    const [staff, total] = await Promise.all([
      Staff.find(filter)
        .populate('classesAssigned', 'name')
        .sort({ lastName: 1 })
        .skip(skip)
        .limit(parseInt(limit)),
      Staff.countDocuments(filter),
    ]);

    res.json({
      success: true,
      data: staff,
      meta: { total, page: parseInt(page), limit: parseInt(limit), pages: Math.ceil(total / parseInt(limit)) },
    });
  } catch (error) {
    next(error);
  }
};

// POST /api/staff
const createStaff = async (req, res, next) => {
  try {
    const { createUserAccount, password, classesAssigned, ...staffData } = req.body;

    const staff = await Staff.create({ ...staffData, classesAssigned: classesAssigned || [] });

    // Optionally create a user login for this staff member
    if (createUserAccount && staffData.email) {
      const plainPassword = password || `Hanara@${new Date().getFullYear()}`;
      const userRole = staffData.role === 'admin' ? 'admin' :
                       staffData.role === 'accountant' ? 'accountant' :
                       staffData.role === 'driver' ? 'driver' : 'teacher';

      await User.create({
        email: staffData.email,
        phone: staffData.phone,
        passwordHash: plainPassword,
        role: userRole,
        refStaff: staff._id,
        isActive: true,
      });
    }

    logger.info(`New staff created: ${staff.firstName} ${staff.lastName}`);
    const populated = await Staff.findById(staff._id).populate('classesAssigned', 'name');
    res.status(201).json({ success: true, data: populated });
  } catch (error) {
    next(error);
  }
};

